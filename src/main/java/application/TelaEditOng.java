    public void ativarCbxCidade(){
        try{
            LocalidadesDAO dao = new LocalidadesDAO();
            for(Cidades cd : dao.buscarCidades()){
               cidadesComboBox.addItem(cd);

               
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Falha ao buscar as cidades cadastradas.\n" + e);
            
        }
    }
     
     
    public void ativarUfs(){
        try{
            LocalidadesDAO dao = new LocalidadesDAO();
            for(UFs uf : dao.buscarUfs()){
               ufsComboBox.addItem(uf);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Falha ao buscar as Unidade Federativa cadastradas.");
        }
    }
     
    public void ativarOngCbx(){
        try{
            OngDAO dao = new OngDAO();
            for(Ong ong : dao.buscarOngs()){
                ongsComboBox.addItem(ong);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Falha ao buscar as ONGs");
        }
    }
    
    public void TextFildInicial(){
        Ong ong = (Ong) ongsComboBox.getSelectedItem();
        idTextField.setText(String.valueOf(ong.getId()));
        idTextField.setEditable(false);
        nomeTextField.setText(ong.getNome());
        emailTextField.setText(ong.getEmail());
        telefoneTextField.setText(ong.getEmail());
        causaTextField.setText(ong.getCausa());
        cnpjTextField.setText(ong.getCnpj());
        siteTextField.setText(ong.getSite());
        cidadesComboBox.setSelectedIndex(-1);
        ufsComboBox.setSelectedIndex(-1);
        
    }
    
    public void attOng()throws Exception{
        
        String id = idTextField.getText();
        String nome = nomeTextField.getText();
        String email = emailTextField.getText();
        String telefone = telefoneTextField.getText();
        String causa = causaTextField.getText();
        String cnpj = cnpjTextField.getText();
        String site = siteTextField.getText();
        Object cidade = cidadesComboBox.getSelectedItem();
        Object uf = ufsComboBox.getSelectedItem();
        
        Ong ong = new Ong();
        
        ong.setId(Integer.parseInt(id));
        ong.setNome(nome);
        ong.setEmail(email);
        ong.setTelefone(telefone);
        ong.setCausa(causa);
        ong.setCnpj(cnpj);
        ong.setSite(site);
        ong.setCidade(String.valueOf(cidade));
        ong.setUf(String.valueOf(uf));
        
        OngDAO dao = new OngDAO();
        dao.attOngs(ong);
    }
    
